/* Data generator: OurAirports CSV -> airports-data.js
 *
 * Builds a worldwide dataset of every airport that has an IATA code.
 * Source (public domain): https://ourairports.com/data/
 *
 * To regenerate:
 *   1. Download the source data:
 *      curl.exe -s -L "https://davidmegginson.github.io/ourairports-data/airports.csv"  -o airports.csv
 *      curl.exe -s -L "https://davidmegginson.github.io/ourairports-data/countries.csv" -o countries.csv
 *   2. node gen-airports.js
 *
 * Emits airports-data.js defining window.PORTS = [ { code, iata, name, city, country, region, type } ].
 */
const fs = require("fs");

// --- Minimal RFC-4180 CSV parser (handles quotes, embedded commas/newlines) ---
function parseCsv(text) {
  const rows = [];
  let row = [];
  let field = "";
  let inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQuotes = false;
      } else field += c;
    } else if (c === '"') {
      inQuotes = true;
    } else if (c === ",") {
      row.push(field); field = "";
    } else if (c === "\n") {
      row.push(field); field = "";
      rows.push(row); row = [];
    } else if (c === "\r") {
      // ignore; handled by \n
    } else {
      field += c;
    }
  }
  if (field.length || row.length) { row.push(field); rows.push(row); }
  return rows;
}

const CONTINENTS = {
  AF: "Africa",
  AN: "Antarctica",
  AS: "Asia",
  EU: "Europe",
  NA: "North America",
  OC: "Oceania",
  SA: "South America",
};

const TYPE_LABEL = {
  large_airport: "Airport",
  medium_airport: "Airport",
  small_airport: "Airport",
  heliport: "Heliport",
  seaplane_base: "Seaplane base",
  balloonport: "Balloonport",
};

const TYPE_RANK = {
  large_airport: 0,
  medium_airport: 1,
  small_airport: 2,
  seaplane_base: 3,
  heliport: 4,
  balloonport: 5,
};

// Country code -> name
const countryRows = parseCsv(fs.readFileSync("countries.csv", "utf8"));
const cHead = countryRows[0];
const cCode = cHead.indexOf("code");
const cName = cHead.indexOf("name");
const countryByCode = {};
for (let i = 1; i < countryRows.length; i++) {
  const r = countryRows[i];
  if (r[cCode]) countryByCode[r[cCode]] = r[cName];
}

// Airports
const rows = parseCsv(fs.readFileSync("airports.csv", "utf8"));
const head = rows[0];
const ix = {
  ident: head.indexOf("ident"),
  type: head.indexOf("type"),
  name: head.indexOf("name"),
  continent: head.indexOf("continent"),
  iso_country: head.indexOf("iso_country"),
  municipality: head.indexOf("municipality"),
  icao_code: head.indexOf("icao_code"),
  iata_code: head.indexOf("iata_code"),
};

const out = [];
let skippedClosed = 0;
let skippedNoIata = 0;
let skippedDup = 0;
for (let i = 1; i < rows.length; i++) {
  const r = rows[i];
  if (!r || r.length < head.length) continue;

  const iata = (r[ix.iata_code] || "").trim().toUpperCase();
  if (!/^[A-Z]{3}$/.test(iata)) { skippedNoIata++; continue; }

  const type = r[ix.type];
  if (type === "closed") { skippedClosed++; continue; }

  const icao = (r[ix.icao_code] || r[ix.ident] || "").trim().toUpperCase();
  const name = (r[ix.name] || "").trim();
  // OurAirports flags erroneous duplicate records with "[Duplicate]" in the name.
  if (/\[duplicate\]/i.test(name)) { skippedDup++; continue; }
  const city = (r[ix.municipality] || "").trim();
  const country = countryByCode[r[ix.iso_country]] || r[ix.iso_country] || "";
  const region = CONTINENTS[r[ix.continent]] || r[ix.continent] || "";

  out.push({
    code: icao,
    iata: iata,
    name: name,
    city: city,
    country: country,
    region: region,
    type: TYPE_LABEL[type] || "Airport",
    _rank: TYPE_RANK[type] != null ? TYPE_RANK[type] : 9,
  });
}

// Sort: major airports first, then by country, then name (nice default result order).
out.sort((a, b) =>
  a._rank - b._rank ||
  a.country.localeCompare(b.country) ||
  a.name.localeCompare(b.name)
);

const clean = out.map((a) => ({
  code: a.code,
  iata: a.iata,
  name: a.name,
  city: a.city,
  country: a.country,
  region: a.region,
  type: a.type,
}));

// Report
const byRegion = {};
for (const a of clean) byRegion[a.region] = (byRegion[a.region] || 0) + 1;
console.log("Total airports with IATA:", clean.length);
console.log("Skipped (no/invalid IATA):", skippedNoIata, "| closed:", skippedClosed, "| duplicates:", skippedDup);
console.log("By region:", JSON.stringify(byRegion));
console.log("No ICAO:", clean.filter((a) => !a.code).length, "| no city:", clean.filter((a) => !a.city).length);
console.log("Sample:", JSON.stringify(clean.slice(0, 3)));

const header = `/* Worldwide airports (every airport with an IATA code).
 * Source (public domain): OurAirports — https://ourairports.com/data/
 * Generated by gen-airports.js. Do not edit by hand; re-run the generator.
 *
 * Each entry: { code (ICAO), iata, name, city, country, region, type }
 */
window.PORTS = [\n`;
const body = clean.map((a) => "  " + JSON.stringify(a) + ",").join("\n");
fs.writeFileSync("airports-data.js", header + body + "\n];\n", "utf8");
console.log("Wrote airports-data.js");
